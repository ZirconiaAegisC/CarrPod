    print("\n[4/5] Building APK...")
    arsc_path = build_binary_resources()
    apk_path = assemble_apk(dex_path, arsc_path)
    
    print("\n[5/5] Signing APK...")
    final_apk = sign_apk(apk_path)
    
    elapsed = time.time() - start_time
